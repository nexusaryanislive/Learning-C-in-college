#include<stdio.h>

int main()
{
    int a = 18;
    float b = 1.80734256;
    char c = 'A';
    double d = 2.9074709709;
    long long int e = 16010123080;

    printf("Actual Integers is : %d  \n", (a));
    printf("Actual Float is : %f  \n", (b));
    printf("Actual Char is : %c  \n", (c));
    printf("Actual Double is : %lf  \n", (d));
    printf("Actual Long Long Integer is : %lld  \n", (e));
}
